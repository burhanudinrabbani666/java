public class EscapeSeqDemo {
    public static void main(String[] args) {

        // "\"<String value>\""
        String name = "\"burhanudin\"";
        System.out.println(name);

        // unicode
        String m = "\u004D";
        System.out.println(m);

        String bani = "\u0042ani"; // Bani
        System.out.println(bani);
    }
}
